CREATE TABLE agencia (
    codigo INTEGER NOT NULL,
    nome CHARACTER VARYING(100),
    endereco CHARACTER VARYING(300),
    cidade CHARACTER VARYING(100),
    estado CHARACTER VARYING(2),
    telefone CHARACTER VARYING(20),
    email CHARACTER VARYING(100),
    CONSTRAINT pk_agencia PRIMARY KEY(codigo)
);

CREATE TABLE cliente (
    id SERIAL,
    cpf CHARACTER VARYING(15) NOT NULL,
    nome CHARACTER VARYING(100),
    telefone CHARACTER VARYING(20),
    email CHARACTER VARYING(100) NOT NULL,
    endereco CHARACTER VARYING(300),
    cidade CHARACTER VARYING(100),
    estado CHARACTER VARYING(2),
    CONSTRAINT pk_cliente PRIMARY KEY(id),
    UNIQUE(cpf)
);

CREATE TABLE conta (
    numero INTEGER NOT NULL,
    senha INTEGER NOT NULL,
    saldo FLOAT NOT NULL,
    tipo CHARACTER VARYING(10),
    cliente_id INTEGER NOT NULL,
    agencia_codigo INTEGER NOT NULL,
    CONSTRAINT pk_conta PRIMARY KEY (agencia_codigo, numero),
    CONSTRAINT fk_cliente FOREIGN KEY(cliente_id) REFERENCES cliente(id),
    CONSTRAINT fk_agencia FOREIGN KEY(agencia_codigo) REFERENCES agencia(codigo)
);

